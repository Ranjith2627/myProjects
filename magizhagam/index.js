$(document).ready(() => {
    let allProjects = "";
    projects.map((project) => {
        allProjects += `
			<div class="col-lg-4 col-md-6 projectLocation" name="${project.link_name}" data-aos="fade-up" data-aos-delay="200">
				<div class="service-box orange">
                    ${project.projectImage}
				  <h3>${project.name}</h3>
				</div>
			</div>
		`;
    });
    $(".projectContainer").html(allProjects);


    $(".projectLocation").click((e) => {
        let location = $(e.currentTarget).attr("name");

        $(".modal-title").text("");
        $(".projectDescription").html("");
        $(".projectImages").html("");

        projects.map((project) => {
            if (project.link_name == location) {
                $(".modal-title").text(project.name);
                $(".projectDescription").html(project.description);
                // $(".projectImages").html(project.image);

                $(`#${location}Modal`).modal("show");
            }
        });


    });

    $(".close").click((e) => {
        $(".modal").modal("hide");
    });
});